    public JFrameDuvida(Duvidas duvida, Aluno aluno) {
        initComponents();
        this.duvida = duvida;
        this.aluno = aluno; 
        setTitle("Duvida " + duvida.getIndagador().getNome());
        criaIndagacao();
        criaRespostas(); 
        jButtonRetomar.setVisible(false);
        if (!duvida.getIndagador().equals(aluno)) {
            jButtonResolver.setVisible(false);
        }
        if (duvida.isResolvido()) {
            jTextFieldResponder.setVisible(false);
            jButtonResponder.setVisible(false); 
            if (!duvida.getIndagador().equals(aluno))
                jButtonRetomar.setVisible(true);
        }
        
    }
    
    public void criaIndagacao() {
        jTextPaneIndagacao.setText(duvida.getIndagacao());
    }
    
    public void criaRespostas() {
        String out = ""; 
        for(Mensagem mensagem:duvida.getComentarios()) {
            if (mensagem.getSujeito() == aluno)
                out += "Você: ";
            else
                out += mensagem.getSujeito().getNome() + ": ";
            out += mensagem.getTexto();
            if (duvida.getComentarios().indexOf(mensagem) != duvida.getComentarios().size()-1) {
                out += "\n";
            }
        }
        jTextPaneRespostas.setText(out); 
    }

